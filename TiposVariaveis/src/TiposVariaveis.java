public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        // Tipos primitivos
        // Tipos inteiros
        byte idade = 123;
        short ano = 2021;
        int cep = 21070333; // se começar com zero, coloca 0x
        long cpf = 98765432109L;
        // Tipos reais
        float pi = 3.14F;
        double salario = 1275.33;
        // Tipo boolean
        boolean verdadeira = true;
        boolean falsa = false;
        // Tipo caractere
        char o = 'o';
        // Tipo texto
        String texto = "Olá, mundo!";

        // Constantes
        final double VALOR_DE_PI = 3.14;
    }
}
