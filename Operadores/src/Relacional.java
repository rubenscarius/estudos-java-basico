public class Relacional {
    public static void main(String[] args) throws Exception {
        // Relacionais
        /*
         * > maior que
         * < menor que
         * >= maior ou igual
         * <= menor ou igual
         * == igual
         * != diferente
         */
        int numero1 = 1;
        int numero2 = 2;
        System.out.println(numero1 != numero2);
    }
}
